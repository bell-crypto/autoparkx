// เรียก DB และฟังก์ชัน sendPush
require __DIR__ . '/db.php';
require __DIR__ . '/push_notify.php';

// -------------------------------
// ตั้ง user id ที่ต้องการทดสอบ
// -------------------------------
$user_id = 3;  // ← แก้เลขเป็น user id จริงที่ต้องการทดสอบ

// -------------------------------
// ดึง push_token จากฐานข้อมูล
// -------------------------------
$stmt = $pdo->prepare("SELECT push_token FROM users WHERE id=?");
$stmt->execute([$user_id]);
$token = $stmt->fetchColumn();

if(!$token){
    die("❌ ยังไม่มี push_token ในผู้ใช้ ID: $user_id<br>ให้เปิดเว็บ/แอปแล้วกด Allow Notification ก่อน");
}

// -------------------------------
// ยิงแจ้งเตือนมาที่มือถือ
// -------------------------------
$result = sendPush($token, "ทดสอบแจ้งเตือน 🎉", "ระบบแจ้งเตือน AutoParkX ทำงานแล้ว 🚗🔥");

// -------------------------------
// แสดงผลลัพธ์ย้อนกลับมาดู
// -------------------------------
echo "<pre>";
print_r($result);
echo "</pre>";

echo "<hr>📲 ถ้า token ถูกต้อง มือถือควรได้รับ Notification ทันที!";
